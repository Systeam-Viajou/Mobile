package com.interdiciplinar.viajou.Models;

public class Photo {
    private int id;
    private String title;
    private String url;
    private String thumbnailUrl;

    // Getters
    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }

    public String getThumbnailUrl() {
        return thumbnailUrl;
    }
}

